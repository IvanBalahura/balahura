package lesson2;// Create a simple program, that creates two integers and print the result of calculation of their addition, substruction, multiplication and division.

public class Task1 {
    public static void main(String[] args) {


        int num1 = 3;
        System.out.print(" first number: "+num1+"\n"  );
        int num2 = 3;
        System.out.print(" second number: "+num2+"\n" );
        System.out.println(num1 + " + " + num2 + " = " +(num1 + num2));

        System.out.println(num1 + " - " + num2 + " = " +(num1 - num2));

        System.out.println(num1 + " x " + num2 + " = " +(num1 * num2));

        System.out.println(num1 + " / " + num2 + " = " +(num1 / num2));



    }
}
